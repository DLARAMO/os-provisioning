namespace Modules\Dreamfiber\Providers;

use Illuminate\Support\ServiceProvider;
use Illuminate\Database\Eloquent\Factory;

class DreamfiberServiceProvider extends ServiceProvider
{
    /**
     * The artisan commands provided by this module
     */
    protected $commands = [
        'Modules\Dreamfiber\Console\OtoFromCsvUpdaterCommand',
    ];

    /**
     * Boot the application events.
     *
     * @return void
     */
    public function boot()
    {
        $this->registerTranslations();
        $this->registerConfig();
        $this->registerViews();
        $this->registerFactories();
        $this->loadMigrationsFrom(module_path('Dreamfiber', 'Database/Migrations'));
    }

    /**
     * Register the service provider.
     *
     * @return void
     */
    public function register()
    {
        $this->app->register(RouteServiceProvider::class);
        $this->commands($this->commands);
    }

    /**
     * Register config.
     *
     * @return void
     */
    protected function registerConfig()
    {
        $this->publishes([
            module_path('Dreamfiber', 'Config/config.php') => config_path('dreamfiber.php'),
        ], 'config');
        $this->mergeConfigFrom(
            module_path('Dreamfiber', 'Config/config.php'), 'dreamfiber'
        );
    }

    /**
     * Register views.
     *
     * @return void
     */
    public function registerViews()
    {
        $viewPath = resource_path('views/modules/dreamfiber');

        $sourcePath = module_path('Dreamfiber', 'Resources/views');

        $this->publishes([
            $sourcePath => $viewPath,
        ], 'views');

        $this->loadViewsFrom(array_merge(array_map(function ($path) {
            return $path.'/modules/dreamfiber';
        }, \Config::get('view.paths')), [$sourcePath]), 'dreamfiber');
    }

    /**
     * Register translations.
     *
     * @return void
     */
    public function registerTranslations()
    {
        $langPath = resource_path('lang/modules/dreamfiber');

        if (is_dir($langPath)) {
            $this->loadTranslationsFrom($langPath, 'dreamfiber');
        } else {
            $this->loadTranslationsFrom(module_path('Dreamfiber', 'Resources/lang'), 'dreamfiber');
        }
    }

    /**
     * Register an additional directory of factories.
     *
     * @return void
     */
    public function registerFactories()
    {
        if (! app()->environment('production') && $this->app->runningInConsole()) {
            app(Factory::class)->load(module_path('Dreamfiber', 'Database/factories'));
        }
    }

    /**
     * Get the services provided by the provider.
     *
     * @return array
     */
    public function provides()
    {
        return [];
    }
}
